"""Уровни факторов оценки Hay Group в виде enum-ов.

Значения совпадают с буквенно-цифровыми кодами подстановочных таблиц,
поэтому их можно использовать напрямую как ключи при поиске баллов.
"""

from __future__ import annotations

from enum import Enum


class StrEnum(str, Enum):
    """str-совместимый Enum (питон 3.11: членов можно сравнивать со строками)."""

    def __str__(self) -> str:  # pragma: no cover - косметика
        return self.value


# ── Фактор 1. Знания и умения (Know-How) ──────────────────────────────────────


class SpecializedKnowHow(StrEnum):
    """Специальные / практические знания (глубина и широта)."""

    A = "A"  # Элементарные базовые
    B = "B"  # Начальные практические
    C = "C"  # Общие практические
    D = "D"  # Продвинутые практические
    E = "E"  # Профессиональные
    F = "F"  # Зрелые профессиональные
    G = "G"  # Всесторонние (несколько доменов)
    H = "H"  # Уникальные, внешне признанные


class ManagerialKnowHow(StrEnum):
    """Управленческие знания: планировать, организовывать, интегрировать."""

    T = "T"   # Отдельные задачи, минимальная интеграция
    I = "I"   # noqa: E741 — уровень Hay «I» (конкретный процесс)
    II = "II"  # Однородная функция
    III = "III"  # Несколько разнородных функций, межфункциональные tradeoff
    IV = "IV"  # Вся организация / крупный бизнес-контур


class Communication(StrEnum):
    """Навыки общения и воздействия."""

    ONE = "1"    # Обмен информацией
    TWO = "2"    # Рациональное влияние, аргументация
    THREE = "3"  # Убеждение, переговоры, изменение поведения


# ── Фактор 2. Решение вопросов (Problem Solving) ──────────────────────────────


class ProblemArea(StrEnum):
    """Область решаемых вопросов (свобода мышления)."""

    A = "A"  # Строго по шаблону
    B = "B"  # По шаблону
    C = "C"  # Процедурная (полушаблонная)
    D = "D"  # Нормативная (полушаблонная)
    E = "E"  # Ясно определённая (цель ясна, путь нет)
    F = "F"  # Широко определённая
    G = "G"  # В целом определённая (стратегия области)
    H = "H"  # Абстрактно определённая


class ProblemComplexity(int, Enum):
    """Сложность решаемых вопросов."""

    REPETITIVE = 1   # Повторяющиеся
    PATTERNED = 2    # Подобные (шаблонные)
    VARIABLE = 3     # Изменчивые
    ADAPTIVE = 4     # Нестандартные
    UNCHARTED = 5    # Неизученные


# ── Фактор 3. Ответственность (Accountability) ────────────────────────────────


class FreedomToAct(StrEnum):
    """Свобода действий."""

    A = "A"  # Строго контролируемая
    B = "B"  # Контролируемая
    C = "C"  # Нормированная
    D = "D"  # Регулируемая в целом
    E = "E"  # Управляемая
    F = "F"  # Управляемая в общих чертах
    G = "G"  # Направляемая
    H = "H"  # Стратегически ориентируемая


class Magnitude(StrEnum):
    """Величина (область) воздействия.

    N — не определена количественно; 1..4 — от очень незначительной до большой.
    """

    N = "N"  # Неопределённая количественно
    ONE = "1"    # Очень незначительная
    TWO = "2"    # Незначительная
    THREE = "3"  # Средняя
    FOUR = "4"   # Большая


class ImpactType(StrEnum):
    """Тип влияния должности на конечный результат."""

    R = "R"  # Remote / незначительное (транзакционное, после факта)
    C = "C"  # Contributory / содействующее (анализ, рекомендации)
    S = "S"  # Shared / совместное (joint KPI, равноправное совладение)
    P = "P"  # Primary / основное (владеет KPI и ресурсами)


class NonQuantitativeImpact(StrEnum):
    """Неколичественная ветка Accountability при Magnitude N.

    Уровни I–VI объединяют область и характер воздействия. Они применяются,
    когда денежный показатель не используется или искажает вклад должности.
    """

    I = "I"      # Вспомогательное
    II = "II"    # Поддерживающее
    III = "III"  # Операционное
    IV = "IV"    # Аналитическое
    V = "V"      # Направляющее
    VI = "VI"    # Воздействующее


# ── Производные ───────────────────────────────────────────────────────────────


class Profile(StrEnum):
    """Краткий профиль должности."""

    A = "A"  # Ответственность выше Решения вопросов
    P = "P"  # Решение вопросов выше Ответственности
    L = "L"  # Сбалансированно


class Confidence(StrEnum):
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


class EvaluationStatus(StrEnum):
    READY = "ready"                       # Готово к оценке / оценка проведена
    NEEDS_CLARIFICATION = "needs_clarification"  # Требуются уточнения
    CANNOT_EVALUATE = "cannot_evaluate"   # Оценка невозможна без данных


class DossierReviewStatus(StrEnum):
    DRAFT_IMPORTED = "draft_imported"      # Импортировано из документа, человек ещё не проверил
    MANUAL_DRAFT = "manual_draft"          # Создано вручную, ещё не проверено Gate 0
    REVIEWED = "reviewed"                  # Человек подтвердил досье


class QCSeverity(StrEnum):
    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"


class QCStatus(StrEnum):
    PASS = "pass"
    WARN = "warn"
    FAIL = "fail"
