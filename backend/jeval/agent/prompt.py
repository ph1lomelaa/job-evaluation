def build_user_message(dossier: JobDossier) -> str:
    """Сериализует JE-досье в компактный JSON для пользовательского сообщения."""
    payload = dossier.model_dump(mode="json", exclude_none=True)
    provenance = _build_provenance_block(dossier)
    return (
        "Оцени следующую должность по методике Hay Group. Выбери уровни факторов и "
        "вызови инструмент submit_evaluation.\n"
        "Смотри не только на поля досье, но и на provenance: откуда взят каждый факт, "
        "какие секции документа его подтверждают.\n\n"
        + provenance
        + "\n\nJE-досье:\n"
        + json.dumps(payload, ensure_ascii=False, indent=2)
    )


def _build_provenance_block(dossier: JobDossier) -> str:
    meta = dossier.import_metadata
    if not meta:
        return "PROVENANCE: нет метаданных импорта."

    lines = ["PROVENANCE:"]
    if meta.source_filename:
        lines.append(f"- source_filename: {meta.source_filename}")
    if meta.source_type:
        lines.append(f"- source_type: {meta.source_type}")
    if meta.source_mime_type:
        lines.append(f"- source_mime_type: {meta.source_mime_type}")
    if meta.source_size_bytes is not None:
        lines.append(f"- source_size_bytes: {meta.source_size_bytes}")
    if meta.source_sha256:
        lines.append(f"- source_sha256: {meta.source_sha256}")
    if meta.field_sources:
        lines.append("- field_sources:")
        for key, values in meta.field_sources.items():
            rendered = "; ".join(values[:3]) if values else "—"
            lines.append(f"  - {key}: {rendered}")
    if meta.notes:
        lines.append("- import_notes:")
        for note in meta.notes[:5]:
            lines.append(f"  - {note}")
    if dossier.organizational_context:
        lines.append(f"- organizational_context: {dossier.organizational_context[:240]}")
    return "\n".join(lines)
