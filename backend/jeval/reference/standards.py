"""Корпоративные документы для контекста агента.

Выжимки составлены из предоставленных пользователем источников. Название
источника включено в текст, чтобы эксперт мог проверить обоснование.
"""

from __future__ import annotations

STANDARDS_VERIFIED = True

CORPORATE_STANDARD_STUB = """\
ИСТОЧНИК: «Тренинг_оценка день 1 — раздатка» и «Шаблон ведомости оценки должностей»:
- Итоговые оценки должностей утверждает Оценочный комитет.
- Вес должности определяется по факторам оценки суммированием баллов по каждой
  оценочной шкале; количество баллов — по точке пересечения показателей факторов.
- Грейд присваивается должности по результатам оценки; разница между баллами
  соседних грейдов — не менее 15%.
- Оценка проводится на основании описания должности либо должностной инструкции;
  описание сопоставляется с описаниями уровней факторов по оценочной шкале.
- Оценивается должность как структурная единица, а не работник."""

EXPERT_GUIDE_STUB = """\
ИСТОЧНИК: «Руководство для эксперта по оценке» и «Подстановочные таблицы Hay Group»:
- Должность оценивается «как она есть сейчас», на дату среза, независимо от
  вознаграждения и статуса, на стандартном уровне эффективности.
- Корректное сравнение должностей зависит от понимания выполняемой работы и
  организационного контекста («нет понимания — нет оценки»).
- Профиль должности (континуум P4…P1, L, A1…A4) показывает природу роли и служит
  встроенной проверкой качества; оценки вне допустимых пределов проверяются.
- Обоснование оценки — постоянная запись суждений экспертов и их причин,
  используется при пересмотрах."""


def build_standards_reference() -> str:
    """Справочный блок корпоративных документов для промпта."""
    return "\n\n".join((CORPORATE_STANDARD_STUB, EXPERT_GUIDE_STUB))
